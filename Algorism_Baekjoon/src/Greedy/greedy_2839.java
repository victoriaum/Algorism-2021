import java.util.Scanner;

public class greedy_2839 {
	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		int cnt =0;
		
		if(n<3) System.out.println(-1);
		else if(3<=n && n<=5000){
			cnt += n/5;
			n=n%5;
			
			if(n%3!=0) cnt += n/3+1;
			else cnt += n/3;
			
			System.out.println(cnt);
		}
		sc.close();
	}
}

